titles_of_currencies = dict((x, y) for x, y in ISO_4217_CURRENCIES)


def get_currency_title(currency_code):
    # for code, title in ISO_4217_CURRENCIES:
    #     if currency_code == code:
    #         return title
    # return None  # 'Unknown currency'
    return titles_of_currencies[currency_code]


# {'AED': 784, 'AFN': 971, 'ALL': 8, 'AMD': 51, ...}
short_titles_and_codes = dict((y[:3], x) for x, y in ISO_4217_CURRENCIES)
# {'AED United Arab Emirates dirham': 784, 'AFN Afghan afghani': 971, 'ALL Albanian lek': 8, ...}
full_titles_and_codes = dict((y, x) for x, y in ISO_4217_CURRENCIES)


def currency_title_to_code(value, plus_curr=None):
    # plus_curr should be a tuple with tuples (first index of every tuple should have a numeric code of code for return
    # that value and second index should have.
    if isinstance(plus_curr, type(tuple())):
        for curr_tuple in plus_curr:
            if value == curr_tuple[1]:
                return curr_tuple[0]

    try:
        value = full_titles_and_codes[value]
    except KeyError:
        try:
            value = short_titles_and_codes[value]
        except KeyError:
            # Just return "value" var, maybe that is already numeric code of currency.
            # Right code of currency or not, should be checked in form.
            pass
    return value
